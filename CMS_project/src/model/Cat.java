package model;

public class Cat {
	private int _id;
	private String _nom;
	private int _ordre;

	public Cat() {
	}

	public int get_id() {
		return _id;
	}

	public void set_id(int _id) {
		this._id = _id;
	}

	public String get_nom() {
		return _nom;
	}

	public void set_nom(String _nom) {
		this._nom = _nom;
	}

	public int get_ordre() {
		return _ordre;
	}

	public void set_ordre(int _ordre) {
		this._ordre = _ordre;
	}

}
