package model;

public class Themes {
private int _id;
private String _nom;
private String _path;

public Themes() {}

public int get_id() {
	return _id;
}

public void set_id(int _id) {
	this._id = _id;
}

public String get_nom() {
	return _nom;
}

public void set_nom(String _nom) {
	this._nom = _nom;
}

public String get_path() {
	return _path;
}

public void set_path(String _path) {
	this._path = _path;
}


}
